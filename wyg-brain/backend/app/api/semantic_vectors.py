class SemanticVectorTable:
    """三维语义向量表

    维度1: 类别向量 — 词所属类别，类别间关联度
    维度2: 属性向量 — 词的属性（人物/地点/物品等），属性间关联度
    维度3: 关联表 — 名人/事件/品牌等关联词到类别和具体词的映射
    """

    def __init__(self):
        self.categories = _CATEGORIES
        self.category_affinity = _get_category_affinity
        self.word_to_category = {}
        for cat in _CATEGORIES:
            # 从 _ATTRIBUTES 中不需要，从外部传入
            pass
        self.word_to_attribute = _WORD_TO_ATTRIBUTE
        self.association_index = _ASSOCIATION_INDEX
        self.char_relations = _CHAR_RELATIONS

    def set_word_categories(self, word_to_cat: dict):
        """设置词→类别映射（从 routes.py 传入）"""
        self.word_to_category = word_to_cat

    def similarity(self, target: str, guess: str, word_categories: dict = None,
                   word_pool: list = None, cilin=None) -> int:
        """计算两词的语义相似度（0-100）

        三维综合：
        1. 关联表查找（最高优先级）
        2. 类别向量 + 属性向量
        3. 词林近义链
        4. 字义关联
        5. 基础分
        """
        if guess == target:
            return 100

        w2c = word_categories or self.word_to_category
        score = 0

        # === 维度3: 关联表查找 ===
        # 猜测词在关联表中
        assoc = self.association_index.get(guess)
        if assoc:
            # 直接关联到目标词
            if target in assoc["words"]:
                score = max(score, assoc["words"][target])
            # 关联到目标类别
            t_cat = w2c.get(target)
            if t_cat and t_cat in assoc["categories"]:
                score = max(score, assoc["categories"][t_cat])

        # 目标词在关联表中（反向）
        assoc_t = self.association_index.get(target)
        if assoc_t:
            if guess in assoc_t["words"]:
                score = max(score, assoc_t["words"][guess])
            g_cat = w2c.get(guess)
            if g_cat and g_cat in assoc_t["categories"]:
                score = max(score, assoc_t["categories"][g_cat])

        if score >= 80:
            return min(100, score)

        # === 维度1: 类别向量 ===
        t_cat = w2c.get(target)
        g_cat = w2c.get(guess)

        # 如果猜测词不在词库，通过关联表推断类别
        if not g_cat and assoc:
            # 取关联表中分数最高的类别
            if assoc["categories"]:
                g_cat = max(assoc["categories"], key=assoc["categories"].get)

        # 词林推断类别（一级+二级+三级）
        if not g_cat and cilin:
            g_cat = self._infer_category(guess, w2c, word_pool or [], cilin)

        if t_cat and g_cat:
            if t_cat == g_cat:
                score = max(score, 65)
            else:
                rel = self.category_affinity(t_cat, g_cat)
                if rel > 0:
                    score = max(score, rel)

        # === 维度2: 属性向量 ===
        t_attr = self.word_to_attribute.get(target)
        g_attr = self.word_to_attribute.get(guess)
        if t_attr and g_attr:
            if t_attr == g_attr:
                score = max(score, 55)
            else:
                rel = _get_attribute_affinity(t_attr, g_attr)
                score = max(score, rel)

        # === 词林近义链 ===
        if score < 30 and cilin:
            try:
                if cilin.is_similar(target, guess):
                    target_syns = set(cilin.get_synonyms(target))
                    if guess in target_syns:
                        score = max(score, 92)
                    else:
                        score = max(score, 78)
                else:
                    # 近义链推断
                    t_syns = set(cilin.get_synonyms(target))
                    g_syns = set(cilin.get_synonyms(guess))
                    if t_syns and g_syns and (t_syns & g_syns):
                        score = max(score, 55)
                    elif t_syns:
                        for gs in g_syns:
                            gs2 = set(cilin.get_synonyms(gs))
                            if t_syns & gs2:
                                score = max(score, 45)
                                break
            except Exception:
                pass

        # === 字符重叠 ===
        if guess in target or target in guess:
            score = max(score, 85)

        target_chars = set(target)
        guess_chars = set(guess)
        common = target_chars & guess_chars
        if common:
            overlap = len(common) / max(len(target_chars), len(guess_chars))
            score = max(score, int(overlap * 75))

        # 首尾字相同
        if len(target) > 1 and len(guess) > 1:
            if target[0] == guess[0]:
                score = max(score, 40)
            if target[-1] == guess[-1]:
                score = max(score, 35)

        # === 字义关联 ===
        if score < 30:
            for tc in target:
                for gc in guess:
                    rel = self.char_relations.get((tc, gc), 0)
                    if rel > 0:
                        score = max(score, rel)
                        break
                if score >= 30:
                    break

        # === 基础分 ===
        if score == 0:
            len_diff = abs(len(target) - len(guess))
            base = max(5, 12 - len_diff * 3)
            score = max(score, base)

        return min(100, score)

    def _infer_category(self, word: str, w2c: dict, word_pool: list, cilin) -> Optional[str]:
        """通过词林推断词语类别（三级推断）"""
        try:
            # 一级：近义词直接在词库中
            syns = cilin.get_synonyms(word)
            for syn in syns:
                inferred = w2c.get(syn)
                if inferred:
                    return inferred
            # 二级：近义词的近义词在词库中
            for syn in syns:
                syns2 = cilin.get_synonyms(syn)
                for syn2 in syns2:
                    inferred = w2c.get(syn2)
                    if inferred:
                        return inferred
            # 三级：is_similar 遍历词库
            for pool_word in word_pool:
                if cilin.is_similar(word, pool_word):
                    return w2c.get(pool_word)
        except Exception:
            pass
        return None


